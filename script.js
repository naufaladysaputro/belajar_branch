function tambah(angka1, angka2) {
    return angka1 + angka2;
  }
  
  function kurang(angka1, angka2) {
    return angka1 - angka2;
  }
  
  // Contoh penggunaan:
  var hasil = tambah(5, 3);
  console.log("Hasil penambahan:", hasil); // Output: Hasil penambahan: 8

  var hasil = kurang(5, 3);
  console.log("Hasil penambahan:", hasil); // Output: Hasil penambahan: 8